package Problem_Set_1;

/**
 * (Area and perimeter of a rectangle) Write a program that displays the area and
 * perimeter of a rectangle with a width of 4.5 and height of 7.9 using the following formula:
 *
 *                                     area = width * height
 */

public class Exercise9 {
    public static void main(String[] args) {
        double width = 4.5;
        double height = 7.9;
        double area = width * height;
        System.out.println("A rectangle with ");
        System.out.println("width: " + width);
        System.out.println("height: " + height);
        System.out.println("area: " + area);
    }
}
